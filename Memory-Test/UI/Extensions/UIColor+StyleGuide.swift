import UIKit

extension UIColor {
    class var backgroundPrimary: UIColor {
        return UIColor(named: "background-primary")!
    }

    class var textPrimary: UIColor {
        return UIColor(named: "primary-text")!
    }

    class var darkSkyBlue: UIColor {
        return UIColor(named: "dark-skyblue")!
    }

    class var textSecondary: UIColor {
        return UIColor(named: "text-secondary")!
    }

    class var cellBackground: UIColor {
        return UIColor(named: "cell-background")!
    }
}
